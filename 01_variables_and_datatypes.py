# ðŸ’¡ Variables are containers for storing data values

# Integer
age = 25
print("Age:", age)

# Float
height = 5.9
print("Height:", height)

# String
name = "Alice"
print("Name:", name)

# Boolean
is_student = True
print("Is student?", is_student)

# Multiple assignments
x, y, z = 1, 2, 3
print("Multiple values:", x, y, z)

# Type checking
print("Type of age:", type(age))
print("Type of name:", type(name))

# Type casting
a = int(4.8)   # Converts float to int
b = str(100)   # Converts int to string
print("After casting:", a, b)
